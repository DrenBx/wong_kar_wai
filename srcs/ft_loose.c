int		ft_loose(int **tab)
{
	int		i;
	int		j;
	int		ok;

	i = 0;
	j = 0;
	ok = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			if ((j + 1 < 4 && tab[i][j] == tab[i][j + 1]) ||
				(i + 1 < 4 && tab[i][j] == tab[i + 1][j]) || tab[i][j] == 0)
				ok = 1;
			j++;
		}
		i++;
	}
	return (ok);
}
