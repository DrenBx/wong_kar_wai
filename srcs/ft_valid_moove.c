int		ft_valid_moove(int **tab, int **save)
{
	int		i;
	int		j;
	int		ok;

	i = 0;
	j = 0;
	ok = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			if (tab[i][j] != save[i][j])
				ok = 1;
			j++;
		}
		i++;
	}
	return (ok);
}
